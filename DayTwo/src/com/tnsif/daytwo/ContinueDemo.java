package com.tnsif.daytwo;

public class ContinueDemo {

	public static void main(String[] args) {

		System.out.println("this is to print continue demo");
		for(int k=1;k<15;k++) {
			if(k%2!=0) {
				System.out.print(" ");
				
			}
			else {
				System.out.print(k);
			}
			
		}
	}

}
